                <p>Selama ini beberapa fakta terkait depresi mungkin sudah banyak diketahui orang. Misalnya, perempuan yang lebih rentan mengalami depresi ketimbang laki-laki.&nbsp;</p>
                
                
                
                <p>Tapi tak hanya itu, ada sejumlah fakta depresi lainnya yang menarik dan kamu ketahui. Simak selengkapnya di bawah ini!</p>
                
                
                
                <h2>Fakta Penting Mengenai Depresi&nbsp;</h2>
                
                
                
                <p>Berikut ini ulasan lengkap mengenai <a href="https://www.halodoc.com/kesehatan/depresi">fakta depresi</a> yang perlu kamu ketahui, di antaranya:</p>
                
                
                
                <h3>1. Memengaruhi kehidupan sehari-hari</h3>
                
                
                
                <p>Depresi umumnya ditandai dengan rasa sedih, serta kehilangan minat terhadap suatu hobi atau aktivitas. Jika dibiarkan terus menerus, hal ini bisa menimbulkan berbagai masalah baik fisik maupun emosional.&nbsp;</p>
                
                
                
                <p>Tak heran jika orang yang mengalami depresi, kerap menarik diri dari lingkungan sosial. Kondisi ini secara langsung bisa mengurangi intensitas komunikasi, baik di tempat kerja maupun sekolah.&nbsp;</p>
                
                
                
                <h3>2. Terbagi menjadi kategori ringan hingga berat</h3>
                
                
                
                <p>Meski terlihat mirip, nyatanya gejala depresi yang muncul pada seseorang sangat bervariasi. Gejala yang muncul bahkan bisa terdiri dari beberapa kategori, mulai dari ringan hingga berat.&nbsp;</p>
                
                
                
                <p>Berikut ini beberapa gejala yang perlu diwaspadai:</p>
                
                
                
                <ul><li>Merasa sedih atau tertekan.</li><li>Perubahan nafsu makan.</li><li>Kehilangan minat terhadap aktivitas.</li><li>Kehilangan atau penambahan berat badan.</li><li>Terlalu banyak atau kurang tidur.</li><li>Tubuh seperti kurang berenergi.</li><li>Mengalami kesulitan dalam berkonsentrasi.</li><li>Mengalami kesulitan dalam membuat suatu keputusan.</li><li>Memiliki pikiran untuk bunuh diri.</li></ul>
                
                
                
                <p>Apabila kamu mengalami salah satu gejala di atas, sebaiknya segera berkonsultasi ke psikolog atau psikiater.&nbsp;</p>
                
                
                
                <p>Dalam beberapa kasus, psikiater akan meresepkan obat bila dianggap perlu. Simak informasi selengkapnya di artikel berikut: “<a href="https://www.halodoc.com/artikel/ini-7-rekomendasi-obat-penenang-yang-aman-atas-anjuran-dokter">7 Rekomendasi Obat Penenang yang Aman atas Anjuran Dokter</a>”.</p>
                
                
                
                <h3>3. Sedih akibat rasa kehilangan tidak sama dengan depresi</h3>
                
                
                
                <p>Saat seseorang mengalami masa sulit, seperti kematian orang terdekat, rasa sedih tentu akan muncul. Ini merupakan hal yang wajar terjadi. Namun, meski ini merupakan bagian dari rasa berduka, faktanya sedih yang muncul akibat kehilangan tidak sama dengan depresi.&nbsp;</p>
                
                
                
                <p>Hal ini karena, sedih yang muncul saat orang berduka umumnya bisa terlihat oleh orang lain dan bersifat temporer atau sementara waktu. Sedangkan orang yang depresi, bisanya bisa menutupi kesedihan seakan kondisinya baik-baik saja di depan orang lain.&nbsp;</p>
                
                
                
                <h3>4. Umumnya dialami oleh dewasa muda</h3>
                
                
                
                <p>Depresi bisa dialami oleh siapa saja dan kapan saja. Namun, gangguan kesehatan mental yang satu ini paling banyak ditemui pada dewasa muda, pertengahan usia 20-an.&nbsp;</p>
                
                
                
                <p>Dewasa muda banyak mengalami depresi akibat adanya perubahan besar dalam hidup mereka. Biasanya ini ditandai dengan munculnya rasa putus asa, sedih terus menerus, menurunnya rasa percaya diri, hingga kecemasan berlebih.&nbsp;</p>
                
                
                
                <p>Pada kondisi tersebut, obat penenang biasanya akan diresepkan oleh dokter. Obat penenang akan bekerja dengan cara memperlambat aktivitas otak, sehingga kecemasan dan insomnia bisa berkurang.&nbsp;</p>
                
                
                
                <p>Jika kamu membutuhkan obat penenang, berikut <a href="https://www.halodoc.com/artikel/ini-pilihan-obat-penenang-depresi-yang-biasa-diresepkan-dokter">Ini Pilihan Obat Penenang Depresi yang Biasa Diresepkan Dokter.&nbsp;</a></p>
                
                
                
                <h3>5. Lebih banyak dialami oleh perempuan</h3>
                
                
                
                <p>Depresi lebih banyak dialami oleh perempuan. Ini jadi salah satu fakta penting mengenai depresi yang perlu untuk diketahui.&nbsp;</p>
                
                
                
                <p>Sebuah studi di tahun 2020 yang diterbitkan oleh <em>American Journal of Psychiatry</em> menyebut, perempuan dua kali lebih banyak mengalami depresi, jika dibandingkan dengan laki-laki.&nbsp;</p>
                
                
                
                <p>Kondisi ini terjadi umumnya pada perempuan muda di usia 14 – 25 tahun, yang kemudian akan menurun seiring bertambahnya usia.&nbsp;</p>
                