<p>Alasan Gen Z Lebih Rentan terhadap Gangguan Mental</p>



<hr class="wp-block-separator">




<p>Perbedaan ini tidak hanya membentuk cara pandang mereka terhadap dunia secara keseluruhan, tetapi juga mempengaruhi kesehatan mental mereka secara signifikan. Gen Z pun sering dikenal sebagai generasi yang lebih sensitif dan rentan mengalami gangguan mental.</p>



<p>Lantas, mengapa Gen Z lebih rentan alami gangguan mental? Simak pembahasannya di bawah ini!</p>



<h3><strong>Gangguan Mental yang Sering Dialami Gen Z</strong></h3>



<p>Menurut penelitian dari American Psychological Association (APA), sekitar 91% Gen Z mengaku pernah mengalami setidaknya satu gejala fisik atau emosional akibat stres. Contohnya seperti merasa <a href="https://www.halodoc.com/kesehatan/depresi">depresi</a> atau sedih dan kehilangan minat, motivasi, atau energi.</p>



<p>Bukan hanya itu, sekitar 1 dari 3 anak muda berusia 18-24 tahun juga melaporkan gejala terkait kesehatan mental, seperti depresi dan <a href="https://www.halodoc.com/kesehatan/gangguan-kecemasan-umum">gangguan kecemasan</a>.</p>



<p>Penelitian lainnya dalam <em>Journal of Abnormal Psychology</em> pada tahun 2019, menunjukkan peningkatan besar dalam tingkat depresi di kalangan remaja dan dewasa muda antara tahun 2009 dan 2017.</p>



<p>Selain itu, data lain menunjukkan bahwa angka bunuh diri pada remaja 15 -19 tahun dan dewasa muda 20 – 24 tahun juga mengalami peningkatan signifikan.</p>



<p>Apabila kamu mengalami depresi atau tanda-tanda gangguan kesehatan mental, segera <a href="https://halodoc.onelink.me/g7yv/7204hmhr">hubungi psikolog atau psikiater</a> di Halodoc. Dalam beberapa kasus, psikiater mungkin saja meresepkan obat-obatan bila dirasa perlu. Baca selengkapnya di artikel ini:</p>



<ul><li><a href="https://www.halodoc.com/artikel/ini-pilihan-obat-penenang-depresi-yang-biasa-diresepkan-dokter">Ini Pilihan Obat Penenang Depresi yang Biasa Diresepkan Dokter</a></li><li><a href="https://www.halodoc.com/artikel/ini-7-rekomendasi-obat-penenang-yang-aman-atas-anjuran-dokter">Ini 7 Rekomendasi Obat Penenang yang Aman atas Anjuran Dokter</a></li></ul>



<h3><strong>Alasan Gen Z Lebih Rentan terhadap Gangguan Mental</strong></h3>



<p>Berikut ini beberapa alasan Gen Z lebih rentan mengalami gangguan <a href="https://www.halodoc.com/kesehatan/kesehatan-mental">kesehatan mental</a>:</p>



<h3><strong>1. Paparan media sosial</strong></h3>



<p>Generasi Z memang menghabiskan lebih banyak waktu di media sosial daripada generasi sebelumnya. Akan tetapi, aktivitas ini juga menjadi faktor yang meningkatkan risiko gangguan mental.</p>



<p>Penelitian menunjukkan bahwa semakin banyak waktu anak muda bermain media sosial, semakin rendah kesehatan mentalnya. Hal ini terjadi karena akses mudah terhadap konten di media sosial, memungkinkan individu terpapar lebih banyak konten negatif.</p>



<p>Selain itu, kehadiran <em>influencer </em>di media sosial sering kali menyebabkan perbandingan fisik dan sosial yang dapat meningkatkan perasaan rendah diri.&nbsp;</p>



<p>Dampak negatif lainnya meliputi <a href="https://www.halodoc.com/kesehatan/insomnia">gangguan tidur</a>, <em>cyberbullying</em>, pelecehan <em>online</em>, permasalahan citra tubuh, perilaku makan tidak sehat, <em>fear of missing out</em> (FOMO), dan gejala depresi.</p>



<p>Untuk lebih jelasnya, kamu bisa membaca artikel berikut ini: “<a href="https://www.halodoc.com/artikel/hubungan-kecanduan-media-sosial-dan-kesehatan-mental">Hubungan Kecanduan Media Sosial dan Kesehatan Mental</a>”.</p>



<h3><strong>2. Memiliki pandangan pesimis terhadap dunia</strong></h3>



<p>Alasan lainnya adalah karena Gen Z memiliki pandangan yang semakin pesimis terhadap dunia.</p>



<p>Menurut studi dari Montclair State University, generasi ini cenderung melihat dunia sebagai tempat yang lebih berbahaya, dibandingkan dengan pandangan generasi sebelumnya yang cenderung lebih positif.</p>



<p>Peristiwa-peristiwa terkini, seperti krisis iklim dan kekerasan yang sering terjadi, membuat Gen Z lebih mudah resah. Belum lagi, generasi ini juga mengalami masa pandemi COVID-19 pada masa pertumbuhan mereka, sehingga menimbulkan pandangan yang cenderung negatif terhadap masa depan.</p>



<h3><strong>3. Terisolasi dari lingkungan</strong></h3>



<p>Menurut survey, hampir separuh responden Generasi Z menggunakan internet 10 jam atau lebih setiap hari. Hal ini menyebabkan waktu yang lebih sedikit untuk berinteraksi secara langsung atau tatap muka dengan orang lain di sekitarnya.</p>



<p>Dampaknya adalah meningkatnya perasaan terisolasi dan kesepian di kalangan Generasi Z. Akibatnya, mereka menjadi lebih rentan terhadap gangguan mental.</p>



<p>Nah, apabila kamu mengalami kesepian, simak pembahasan pada artikel ini untuk mengetahui cara mengatasinya: <a href="https://www.halodoc.com/artikel/lawan-kesepian-dengan-4-cara-sederhana-ini">“Lawan Kesepian dengan 4 Cara Sederhana Ini”</a>.</p>



<h3><strong>4. Lebih peduli terhadap isu sosial dan politik</strong></h3>



<p>Menurut penelitian dari Edelman, sekitar 70% dari Generasi Z di seluruh dunia mengatakan mereka terlibat dalam isu sosial atau politik. Kepedulian ini sering kali dipicu oleh mudahnya akses mereka terhadap berita terbaru melalui internet.</p>



<p>Akan tetapi, hal ini juga membuat Gen Z terpapar secara intensif pada berbagai isu sensitif. Contohnya seperti perang, kekerasan, konflik politik, rasisme, dan masalah-masalah sosial lainnya yang sering kali belum terselesaikan oleh pemerintah.</p>



<p>Lingkungan politik dan sosial yang tidak stabil dapat memperburuk kecemasan dan juga mendorong pikiran pesimis dari Gen Z.</p>



<h3><strong>5. Ketidakpastian masa depan</strong></h3>



<p>Generasi Z tidak hanya menghadapi tantangan dalam isu-isu sosial dan politik, tetapi juga dari perubahan yang cepat dalam masyarakat dan teknologi.</p>



<p>Kemajuan <em>Artificial Intelligence</em> (AI) yang mengancam pekerjaan tradisional dan kesulitan mendapatkan pekerjaan, menambah tekanan psikologis pada mereka yang baru memasuki dunia kerja. Selain itu, biaya perumahan dan kebutuhan dasar yang semakin melonjak juga menambah beban finansial yang mereka tanggung.</p>



<p>Ketidakpastian akan masa depan dan perubahan konstan ini seringkali memicu stres di kalangan Generasi Z.</p>